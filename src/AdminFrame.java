    private void btnHandleRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHandleRequestActionPerformed
int row = tblRequests.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Please select a request.");
    return;
}

int appId = (int) tblRequests.getValueAt(row, 0);
String name = tblRequests.getValueAt(row, 1).toString();
String role = tblRequests.getValueAt(row, 2).toString();
String type = tblRequests.getValueAt(row, 3).toString();
String details = tblRequests.getValueAt(row, 4).toString();

// Build application object
Application app = new Application(role, type, details);

// Build the chain
RequestHandler leave = new LeaveRequestHandler();
RequestHandler certificate = new CertificateRequestHandler();
RequestHandler rechecking = new RecheckingRequestHandler();

leave.setNextHandler(certificate);
certificate.setNextHandler(rechecking);

boolean result = leave.handle(app);

if (result) {
    // Update DB
    try {
        Connection conn = DatabaseConnectivity.getInstance().getConnection();
        String sql = "UPDATE applications SET status = 'Approved' WHERE id = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, appId);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Request Approved.");
        loadRequests(); // refresh table
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
} else {
    JOptionPane.showMessageDialog(this, "Unhandled request type.");
}
        
    }//GEN-LAST:event_btnHandleRequestActionPerformed


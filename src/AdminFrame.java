    public void loadRequests() {
    try {
        DefaultTableModel model = (DefaultTableModel) tblRequests.getModel();
        tblRequests.getColumnModel().getColumn(0).setPreferredWidth(40);
tblRequests.getColumnModel().getColumn(1).setPreferredWidth(150);
tblRequests.getColumnModel().getColumn(2).setPreferredWidth(100);
tblRequests.getColumnModel().getColumn(3).setPreferredWidth(120);
tblRequests.getColumnModel().getColumn(4).setPreferredWidth(250);
        model.setRowCount(0);
        Connection conn = DatabaseConnectivity.getInstance().getConnection();
        String sql = "SELECT * FROM applications WHERE status = 'Pending'";
        ResultSet rs = conn.createStatement().executeQuery(sql);
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("applicant_name"),
                rs.getString("role"),
                rs.getString("type"),
                rs.getString("details")
            });
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }
}

